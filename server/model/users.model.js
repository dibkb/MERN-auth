import mongoose from "mongoose";
const usersSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: false,
  },
});
export const usersModel = mongoose.model("Users", usersSchema);
